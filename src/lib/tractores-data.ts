/**
 * Strips common noise words and symbols to get the core tractor identity.
 * Extremely aggressive to ensure matches across variations in filenames.
 */
const cleanForMatching = (s: string) => {
    return s.toLowerCase()
        .replace(/\.[a-z0-9]+$/i, '') // Remove extension
        .replace(/[-_.]/g, ' ') // Normalize separators
        .replace(/\b(brochure|broch|folleto|catalogo|flyer|lowres|spanish|my\d+|range|spec|sheet|launch|document|target|trend|v\d+|ub\d*)\b/gi, '')
        .replace(/[^a-z0-9]/g, ''); // Final nuclear pass
};

const NORMALIZED_TRACTOR_NAMES = Object.entries(SPECIFIC_TRACTOR_NAMES).reduce((acc, [key, value]) => {
    acc[cleanForMatching(key)] = value;
    return acc;
}, {} as Record<string, string>);

const NORMALIZED_IGNORED = IGNORED_CATALOG_FILES.map(cleanForMatching);

/**
 * Normalizes a filename or brand-specific code into a user-friendly name.
 * Handles extensions, technical prefixes, and specific mapping overrides.
 */
export function getTractorFormattedName(originalFilename: string): string {
    let baseName = originalFilename;
    while (baseName.includes('.')) {
        const parts = baseName.split('.');
        const lastPart = parts[parts.length - 1];
        // If the last part is clearly an extension (short)
        if (lastPart && lastPart.length < 10) {
            const lastDot = baseName.lastIndexOf('.');
            baseName = baseName.substring(0, lastDot);
        } else {
            break;
        }
    }
    
    // Also remove common suffixes like .inline or .inline (1)
    baseName = baseName.replace(/\.inline(\s\(\d+\))?$/i, '');

    const searchKey = cleanForMatching(baseName);
    
    // Check if it should be ignored (e.g. duplicates)
    if (NORMALIZED_IGNORED.includes(searchKey)) {
        return "__IGNORE__";
    }

    // 1. Indestructible match via cleaned mapping
    if (NORMALIZED_TRACTOR_NAMES[searchKey]) {
        return NORMALIZED_TRACTOR_NAMES[searchKey];
    }

    // 2. Partial match for complex or concatenated codes
    for (const [key, value] of Object.entries(NORMALIZED_TRACTOR_NAMES)) {
        if (key.length > 5 && (searchKey.includes(key) || key.includes(searchKey))) {
            return value;
        }
    }

    // Fallback logic for unmapped files (like Mc Cormick)
    let cleaned = baseName.replace(/[-_]/g, " ");
    cleaned = cleaned.replace(/\b(tractor|tractores|folleto|catalogo|ficha|tecnica|brochure|series|lowres|flyer|stage|v|spanish|english|my\d+|hr|web|inline)\b/gi, "");
    
    // Specifically for Mc Cormick, they use MC_ prefix
    cleaned = cleaned.replace(/\bmc\b/gi, "");

    // Remove technical codes like 308.8901.4.2.0 or CP05, RP6D
    const parts = cleaned.split(" ").filter(p => {
        const isTechnical = /^[A-Z0-9.]+$/i.test(p) && /\d/.test(p) && p.length < 8;
        const isNumeric = /^[0-9.]+$/.test(p);
        return !isTechnical && !isNumeric && p.length > 0;
    });
    
    const result = parts.map(w => w.toUpperCase()).join(" ");
    return result || "CATÁLOGO";
}

export function generateTractorFriendlySlug(formattedName: string): string {
    return formattedName
        .toLowerCase()
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "")
        .replace(/\./g, "-")
        .replace(/[^a-z0-9]+/g, "-")
        .replace(/^-+|-+$/g, "");
}
